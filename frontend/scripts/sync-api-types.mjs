import { mkdir, rm, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const swaggerUrl = process.env.CARD_NOTES_SWAGGER_URL ?? 'http://localhost:3000/api/docs-json';
const scriptDirectory = path.dirname(fileURLToPath(import.meta.url));
const outputRoot = path.resolve(scriptDirectory, '../src/types/api');

let response;

try {
  response = await fetch(swaggerUrl);
} catch (error) {
  const message = error instanceof Error ? error.message : String(error);

  throw new Error(`Could not fetch swagger from ${swaggerUrl}: ${message}`);
}

if (!response.ok) {
  throw new Error(`Could not fetch swagger from ${swaggerUrl}: ${response.status}`);
}
const swagger = await response.json();
const schemas = swagger.components?.schemas ?? {};
const schemaNames = Object.keys(schemas).sort((left, right) => left.localeCompare(right));

if (schemaNames.length === 0) {
  throw new Error(`Swagger document from ${swaggerUrl} does not expose component schemas.`);
}

function fileBase(name) {
  return name
    .replace(/Dto$/, '')
    .replace(/([a-z0-9])([A-Z])/g, '$1-$2')
    .replace(/([A-Z]+)([A-Z][a-z])/g, '$1-$2')
    .toLowerCase();
}

function refName(ref) {
  return ref.split('/').at(-1);
}

function literal(value) {
  return JSON.stringify(value);
}

function isRecord(value) {
  return typeof value === 'object' && value !== null && !Array.isArray(value);
}

function collectRefs(schema, refs = new Set()) {
  if (!isRecord(schema)) return refs;

  if (schema.$ref) {
    refs.add(refName(schema.$ref));
    return refs;
  }

  for (const value of Object.values(schema)) {
    if (Array.isArray(value)) {
      value.forEach((item) => collectRefs(item, refs));
    } else {
      collectRefs(value, refs);
    }
  }

  return refs;
}

function schemaToType(schema) {
  if (!isRecord(schema)) return 'unknown';

  const type = schemaToNonNullableType(schema);

  return schema.nullable ? `${type} | null` : type;
}

function schemaToNonNullableType(schema) {
  if (schema.$ref) return refName(schema.$ref);
  if (schema.allOf) return schema.allOf.map(schemaToType).join(' & ');
  if (schema.oneOf) return schema.oneOf.map(schemaToType).join(' | ');
  if (schema.anyOf) return schema.anyOf.map(schemaToType).join(' | ');
  if (schema.enum) return schema.enum.map(literal).join(' | ');
  if (schema.type === 'array') return `Array<${schemaToType(schema.items)}>`;

  if (schema.type === 'object' || schema.properties || schema.additionalProperties) {
    return objectSchemaToType(schema);
  }

  if (schema.type === 'integer' || schema.type === 'number') return 'number';
  if (schema.type === 'boolean') return 'boolean';
  if (schema.type === 'string') return 'string';
  if (schema.type === 'null') return 'null';
  if (Array.isArray(schema.type)) return schema.type.map((item) => schemaToType({ ...schema, type: item })).join(' | ');

  return 'unknown';
}

function objectSchemaToType(schema) {
  if (!schema.properties) {
    return `Record<string, ${additionalPropertiesToType(schema.additionalProperties)}>`;
  }

  const required = new Set(schema.required ?? []);
  const lines = Object.entries(schema.properties).map(([key, value]) =>
    `  ${JSON.stringify(key)}${required.has(key) ? '' : '?'}: ${schemaToType(value)};`,
  );

  if (schema.additionalProperties) {
    lines.push(`  [key: string]: ${additionalPropertiesToType(schema.additionalProperties)};`);
  }

  return `{
${lines.join('\n')}
}`;
}

function additionalPropertiesToType(additionalProperties) {
  if (additionalProperties === true) return 'unknown';
  if (isRecord(additionalProperties)) return schemaToType(additionalProperties);

  return 'unknown';
}

function schemaToDeclaration(name, schema) {
  return `export type ${name} = ${schemaToType(schema)};`;
}

function generatedContent(name, schema, imports) {
  return `// Generated by scripts/sync-api-types.mjs from ${swaggerUrl}.
// Do not edit by hand.
${imports ? `\n${imports}\n` : ''}
${schemaToDeclaration(name, schema)}
`;
}

await rm(outputRoot, { recursive: true, force: true });
await mkdir(outputRoot, { recursive: true });

for (const name of schemaNames) {
  const schema = schemas[name];
  const refs = [...collectRefs(schema)]
    .filter((ref) => ref !== name && schemaNames.includes(ref))
    .sort((left, right) => left.localeCompare(right));
  const imports = refs
    .map((ref) => `import type { ${ref} } from './${fileBase(ref)}.dto';`)
    .join('\n');

  await writeFile(
    path.join(outputRoot, `${fileBase(name)}.dto.ts`),
    generatedContent(name, schema, imports),
    'utf8',
  );
}

const exports = schemaNames
  .map((name) => `export type * from './${fileBase(name)}.dto';`)
  .join('\n');
await writeFile(path.join(outputRoot, 'index.ts'), `${exports}\n`, 'utf8');

console.log(`Wrote generated API types to ${outputRoot}`);
